#include "libft.h"

int		ft_countnumbers(int n)
{
	int		i;

	if (n == -2147483648)
		return (11);
	i = 0;
	if (n <= 0)
	{
		i++;
		n *= (-1);
	}
	while (n > 0)
	{
		n = n / 10;
		i++;
	}
	return (i);
}
