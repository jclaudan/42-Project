#include "libft.h"

void		ft_lstrev(t_list **alst)
{
	t_list	*cur;
	t_list	*next;
	t_list	*prev;

	cur = *alst;
	prev = NULL;
	while (cur != NULL)
	{
		next = cur->next;
		cur->next = prev;
		prev = cur;
		cur = next;
	}
	*alst = prev;
}
